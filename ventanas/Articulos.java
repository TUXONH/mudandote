    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        int idx = (int) tabla.getValueAt(tabla.getSelectedRow(),0);
        clases.conexion con = new clases.conexion();
        try {            
            PreparedStatement pstm =(PreparedStatement)                    
            con.getConnection().prepareStatement("UPDATE catarticulos SET Descripcion='"+descripcion.getText()+"',Familia='"+familia.getSelectedItem().toString()+"',M3='"+m3.getText()+"',Servicios='"+Servicios.getSelectedItem().toString()+"' WHERE Clave="+idx);                                                        
            pstm.executeUpdate();
            pstm.close();                        
            Main2 main = new Main2();
            main.pasar_valores(tabla, sql2);
            JOptionPane.showMessageDialog(null,"Se modifico el registro con exito!");
            
            aceptar.setVisible(false);
            cancelar.setVisible(false);
            //buscar.setVisible(true);
            eliminar.setVisible(true);
            agregar.setVisible(true);
            modificar.setVisible(true);
            descripcion.setText("");
        familia.setSelectedIndex(0);
        m3.setText("");
        Servicios.setSelectedIndex(0);
        } catch (SQLException ex) {
            Logger.getLogger(Articulos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_aceptarActionPerformed

